function sum(a, b) {
    return a + b
}


// arrow
const sum  = (a, b) => a + b;


// function declaraction
function factorial(num) {
    let result = 1;

    for (let i = 2; i <= num; i++) {
        result *= i;
    }

    return result;
}

// arrow function

const factorial = (num) => {
    let result = 1;

    for (let i = 2; i <= num; i++) {
        result *= i;
    }

    return result;
}

