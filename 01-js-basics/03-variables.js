//1. let allows us to change the value of the variable

let firstName = 'John';

console.log(firstName); // 'John'

// Re-assign a new value to a variable
firstName = 'James';

console.log(firstName)

//2. const stands for constant 
const pi = 3.14;
console.log(pi);


//3. var
var lastName = 'Doe';

lastName = 'Smith';

console.log(lastName); // 'Smith'

// Another differnce bt let and const
let city = 'Chicago'; // variable initialization = declare a variable and assign a value

let country; // declaring a variable
country = 'US'; // value assignment

// const firstMonth; // Not allowed
